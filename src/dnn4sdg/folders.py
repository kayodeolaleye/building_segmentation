def train_data_provider():
    img_folder='onitemi/data/mbd/training/'
    label_folder='onitemi/data/mbd/training/building_bitmaps/'
    cache_dir = '.cache/mbd'
    fout=join(cache_dir,'train.pkl')   
    try:
        img,lbl=pickle.load(open(fout,'rb'))
    except:    
        img,lbl=ImageDataGenerator(img_folder,label_folder,stride=10,tol=3,flip=True,mirror=True,kayode=True)
        pickle.dump((img,lbl), open(fout,'wb'))
        
    return img,lbl
    

